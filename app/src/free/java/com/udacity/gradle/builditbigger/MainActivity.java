import static br.com.lucaslprimo.jokeshow.JokeShowActivity.EXTRA_JOKE;


public class MainActivity extends AppCompatActivity implements EndpointsAsyncTask.CallbackAsyncTask{

    RelativeLayout loading;
    private InterstitialAd mInterstitialAd;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        loading = findViewById(R.id.layoutLoading);

        mInterstitialAd = new InterstitialAd(this);
        mInterstitialAd.setAdUnitId("ca-app-pub-3940256099942544/1033173712");
        mInterstitialAd.loadAd(new AdRequest.Builder().build());

        mInterstitialAd.setAdListener(new AdListener() {
            @Override
            public void onAdClosed() {
                // Load the next interstitial.
                mInterstitialAd.loadAd(new AdRequest.Builder().build());
            }

        });
    }


    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.menu_main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.action_settings) {
            return true;
        }

        return super.onOptionsItemSelected(item);
    }

    public void tellJoke(View v)
    {
        if (mInterstitialAd.isLoaded())
            mInterstitialAd.show();
        loading.setVisibility(View.VISIBLE);
        new EndpointsAsyncTask(this).execute();
    }

    void goToTheJokeActivity(String joke){

        Intent intent = new Intent(this, JokeShowActivity.class);
        intent.putExtra(EXTRA_JOKE,joke);

        startActivity(intent);
    }

    @Override
    public void onFinish(String joke) {
        loading.setVisibility(View.GONE);
        goToTheJokeActivity(joke);
    }
}




